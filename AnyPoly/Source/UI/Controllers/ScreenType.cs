namespace AnyPoly.UI;

/// <summary>
/// Enumeration of the different screen types.
/// </summary>
internal enum ScreenType
{
    /// <summary>
    /// The screen is displayed as a window.
    /// </summary>
    Windowed,

    /// <summary>
    /// The screen is displayed in fullscreen mode.
    /// </summary>
    FullScreen,

    /// <summary>
    /// The screen is displayed in borderless mode.
    /// </summary>
    Borderless,
}